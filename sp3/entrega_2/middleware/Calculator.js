export class Calculator {
  add(numbers) {
    console.log("Adding both numbers gives:")
    return numbers.num1 + numbers.num2;
  }
  multiply(numbers) {
    console.log("Multiplying both numbers gives:")
    return numbers.num1 * numbers.num2;
  }
  substract(numbers) {
    console.log("Substracting both numbers gives:")
    return numbers.num1 + numbers.num2;
  }
}
